void self_check(){
	unsigned char i;
	ledr_on();
	P10=0;
	for(i=8;i;i--){
		delay_ms(1000);
		P1 <<=1;
		P1++;
		rgbrr();
	}
	P20=0;
	for(i=8;i;i--){
		delay_ms(1000);
		P2 <<=1;
		P2++;
		rgbrr();
	}	
	P3 |= 0x70; //ledrgb_off();
}

void init(){
	unsigned char i;
	P1=P2=P3=0xff;
	//int0 init
  INT0 = 1;										//setbit P32
  IT0 = 1;                    //INT0.set to down lip
  EX0 = 1;                    //INT0.enable
  EA = 1;
	//t0 init
  AUXR &= 0x7f;                   //set t0 to mode12T
  TMOD = 0x00;                    //set t0 to mode0
  TL0 = T1MS;                     //time register init
  TH0 = T1MS >> 8;
  TR0 = 1;                        //start t0
  ET0 = 1;                        //t0 int enable
  //EA = 1;

	for(i=0;i<=DEGREE_DIVMAX;i++){
		Pone[i]=~Pone[i];
		Ptwo[i]=~Ptwo[i];
	}
}

void delay_ms(unsigned int ms){
     unsigned int i;
	 do{
	      i = MAIN_Fosc / 13000;
		  while(--i)	;   //14T per loop
     }while(--ms);
}
void keydetect_on(){
	while(KEY==T_OFF);
	motor_on();
	while(spd_t>SPD_TWORK);
	rounddisplay();
}
void rounddisplay(){
	unsigned char i;
	ledr_on();
	P10=0;
	for(i=8;i;i--){
		delay_ms(500);
		P1 <<=1;
		P1++;
	}
	P20=0;
	for(i=8;i;i--){
		delay_ms(500);
		P2 <<=1;
		P2++;
	}
	delay_ms(500);
	P27=0;
	for(i=8;i;i--){
		delay_ms(500);
		P2 >>=1;
		P2+=0x80;
	}
	P17=0;
	for(i=8;i;i--){
		delay_ms(500);
		P1 >>=1;
		P1+=0x80;
	}
	ledr_off();	
}
void chardisplay(){
	unsigned char i;
	//while(KEY==T_OFF);
	delay_ms(1000);
	while(spd_t>SPD_TWORKC);
	ledr_on();
	while(KEY==T_OFF){
		while(LOCASIGNAL==LOCA_OFF);
		for(i=0;i<DEGREE_DIVMAX;i++){
			P1=Pone[i];
			P2=Ptwo[i];
			delay_ms(spd_t*(14.0/DEGREE_DIVMAX));//(14.0ms<20ms)
		}
		P1=0xff;
		P2=0xff;
	}	
}
void picdisplay(){
	unsigned char i;
	delay_ms(1000);
	while(spd_t>SPD_TWORKC);
	while(KEY==T_OFF){
		while(LOCASIGNAL==LOCA_OFF);
		ledr_on();
		for(i=0;i<DEGREE_DIVMAX-1;i++){
			if(i==DEGREE_DIVMAX/4){ledr_off();ledg_on();}
			if(i==DEGREE_DIVMAX/2) ledr_on();
			if(i==DEGREE_DIVMAX*3/4){ledr_off();ledg_off();ledb_on();}
			P1=Pone_p[i];
			P2=Ptwo_p[i];
			delay_ms(spd_t*(19.0/DEGREE_DIVMAX));//(19.0ms<20ms)
		}
		ledb_off();
		P1=0xff;
		P2=0xff;
	}	
}

